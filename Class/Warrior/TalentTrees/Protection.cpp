void Protection::add_shield_slam(QMap<QString, Talent*>& talent_tier) {
    auto talent = get_new_talent(warrior, "Shield Slam", "7ML", base_url + "items/Inv_shield_05.png", 1,
                                 "Slam the target with your shield, causing 225 to 236 damage, modified by your shield block value, and has a 50%"
                                 "chance of dispelling 1 magic effect on the target. Also causes a high amount of threat.",
                                 QVector<QPair<unsigned, unsigned>>(),
                                 QVector<SpellRankGroup*> {spells->get_spell_rank_group_by_name("Shield Slam")});

    add_talent_to_tier(talent_tier, talent);
}

void Protection::add_improved_shield_block(QMap<QString, Talent*>& talent_tier) {
    QMap<unsigned, QString> rank_descriptions;
    QString base_str = "Allows your Shield Block ability to block an additional attack and increases the duration by %1 seconds.";
    rank_descriptions.insert(0, base_str.arg(0.5));
    rank_descriptions.insert(1, base_str.arg(0.5));
    rank_descriptions.insert(2, base_str.arg(1));
    rank_descriptions.insert(3, base_str.arg(2));
    Talent* talent = new Talent(warrior, this, "Improved Shield Block", "3ML", base_url + "ability/Ability_defend.png", 3, rank_descriptions, {});

    add_talent_to_tier(talent_tier, talent);
}

void Protection::add_improved_sunder_armor(QMap<QString, Talent*>& talent_tier) {
    auto talent = get_new_talent(warrior, "Improved Sunder Armor", "4LL", base_url + "ability/Ability_warrior_sunder.png", 3,
                                 "Reduces the cost of your Sunder Armor ability by %1 rage.", QVector<QPair<unsigned, unsigned>> {{1, 1}},
                                 QVector<SpellRankGroup*> {spells->get_spell_rank_group_by_name("Sunder Armor")});

    add_talent_to_tier(talent_tier, talent);
}

